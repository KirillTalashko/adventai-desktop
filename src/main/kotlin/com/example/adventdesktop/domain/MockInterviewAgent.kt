package com.example.adventdesktop.domain

/**
 * Агент пробного собеседования: играет визового офицера консульства в отдельном окне. Ведёт живой диалог
 * (по одному вопросу за раз) и по запросу даёт оценку готовности. Это side-сессия — она НЕ меняет
 * состояние задачи; основной автомат остаётся на своём шаге.
 */
class MockInterviewAgent(private val gateway: LlmGateway) {

    /** Очередной вопрос офицера по текущей истории собеседования (пустая история → первый вопрос). */
    suspend fun turn(task: String, history: List<Message>): GatewayResponse =
        gateway.complete(listOf(Message(Role.System, interviewer(task))) + history)

    /** Завершить собеседование и дать оценку готовности. */
    suspend fun evaluate(task: String, history: List<Message>): GatewayResponse =
        gateway.complete(listOf(Message(Role.System, interviewer(task))) + history + Message(Role.User, EVAL))

    private fun interviewer(task: String): String =
        "Ты — визовый офицер консульства, проводишь ПРОБНОЕ собеседование, чтобы потренировать заявителя. " +
            "Контекст задачи пользователя: «$task». Веди собеседование вживую: задавай по ОДНОМУ вопросу за раз " +
            "(типичные вопросы консула: цель и сроки поездки, финансирование, связи с родиной, маршрут и брони, " +
            "место работы/учёбы). Коротко реагируй на ответ и задавай следующий вопрос. Всего 5–6 вопросов, затем " +
            "напиши, что собеседование можно завершить и получить оценку. Пиши по-русски, естественно и по делу."

    private companion object {
        const val EVAL = "Заверши пробное собеседование и дай оценку: вердикт готовности " +
            "(Готов / Есть пробелы / Не готов), 2–3 сильные и 2–3 слабые стороны ответов и краткий совет. " +
            "По-русски, сжато."
    }
}
