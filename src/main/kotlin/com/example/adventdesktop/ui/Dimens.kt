package com.example.adventdesktop.ui

import androidx.compose.ui.unit.dp

/**
 * Единый источник токенов радиусов скруглений (аудит Рамса, #3 aesthetic) — чтобы они не «дрейфовали»
 * по коду. Раньше в UI встречалось 7 разных радиусов (6/8/9/10/12/16/20); здесь набор сведён к
 * семантической шкале в духе `DESIGN_BRIEF.md` («скругления 10–16»): 6→xs(8), 9→sm(10).
 *
 * Шкала отступов (4/8/12/16/24) — отдельный отложенный шаг: массовая замена `padding`/`spacedBy`
 * рискованна без визуальной проверки (те же числа встречаются как размеры, не только как отступы).
 */
object Radii {
    val xs = 8.dp   // мелкие пилюли/чипы/статусы
    val sm = 10.dp  // кнопки, айтемы списка, дропдауны
    val md = 12.dp  // карточки, пользовательский «пузырь» примеров
    val lg = 16.dp  // композер, бабл сообщения, карточки памяти
    val xl = 20.dp  // онбординг-карта
}
